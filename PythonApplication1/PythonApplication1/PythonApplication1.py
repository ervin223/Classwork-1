# Запросить количество учащихся в классе
n = int(input("Введите количество учащихся в классе: "))

# Инициализировать переменную для суммы роста
total_height = 0

# Вводить данные о росте для каждого учащегося и добавлять их к сумме
for i in range(n):
    height = float(input(f"Введите рост учащегося {i + 1} (в сантиметрах): "))
    total_height += height

# Рассчитать средний рост
average_height = total_height / n

# Вывести результат
print(f"Средний рост учащихся класса: {average_height:.2f} см")
